package Data_Structures;

import java.util.*;
public class HashSetDemo {
	public static void main(String args[])
	{
		HashSet<String> hs = new HashSet<String>(); 
		hs.add("dfsf");
		hs.add("dsfsdfd");
		hs.add("dsdds");
		hs.add("dfsf");
		System.out.println("content and size of the hashset is " + hs + hs.size());
	}
	
	
	

}
